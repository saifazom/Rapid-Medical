/*
 * @hooked WC_Emails::email_header() Output the email header
 */
do_action( 'woocommerce_email_header', $email_heading, $email ); ?>

<?php /* translators: %s: Customer first name */ ?>
<p><?php printf( esc_html__( 'Hi %s,', 'woocommerce' ), esc_html( $order->get_billing_first_name() ) ); ?></p>
<?php /* translators: %s: Order number */ ?>
<p><?php printf( esc_html__( 'Your order #%s has been processed:', 'woocommerce' ), esc_html( $order->get_order_number() ) ); ?></p>

<?php

/*
 * @hooked WC_Emails::order_details() Shows the order details table.
 * @hooked WC_Structured_Data::generate_order_data() Generates structured data.
 * @hooked WC_Structured_Data::output_structured_data() Outputs structured data.
 * @since 2.5.0
 */
do_action( 'woocommerce_email_order_details', $order, $sent_to_admin, $plain_text, $email );

/*
 * @hooked WC_Emails::order_meta() Shows order meta data.
 */
do_action( 'woocommerce_email_order_meta', $order, $sent_to_admin, $plain_text, $email );

/**
 * Check if there have a donation payment. If so then add a thank you message.
 */

$is_donation = false;
$donation_product_id = get_field('donation_product', 'options');

$items = $order->get_items(); 
foreach ( $items as $item_id => $item ) {
    if($donation_product_id == $item['product_id']){
        $is_donation = true;
    }
}

if($is_donation){
    echo '<p>Thank you for your donation to Inner Light Ministries. ILM is a 501(c)(3) non profit ecclesiastical organization. All contributions are tax deductible. All contributions help ILM further its service in the world. With your assistance, we are able to provide a website, video broadcasts & recordings, audio recordings, books & CDâ€™s, newsletters, iTunes podcasts, classes, workshops, retreats, scholarships, counseling, and other services! We appreciate your support!</p>';
}

/*
 * @hooked WC_Emails::customer_details() Shows customer details
 * @hooked WC_Emails::email_address() Shows email address
 */
do_action( 'woocommerce_email_customer_details', $order, $sent_to_admin, $plain_text, $email );

/**
 * Show user-defined additional content - this is set in each email's settings.
 */
if ( $additional_content ) {
	echo wp_kses_post( wpautop( wptexturize( $additional_content ) ) );
}

/*
 * @hooked WC_Emails::email_footer() Output the email footer
 */
do_action( 'woocommerce_email_footer', $email );
